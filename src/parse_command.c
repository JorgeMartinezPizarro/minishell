#include "libft.h"

///////////////////////////////////////////////////////////////////////////////
//
///////////////////////////////////////////////////////////////////////////////
void	parse_command(char *line)
{
	char **strings;

	strings = ft_split(line, ' ');
	if (ft_strncmp(strings[0], "echo", 4) == 0)
		ft_printf("%s\n", strings[1]);
	else
		ft_printf("Command '%s' not found\n", strings[0]);
	
}
